using System.Runtime.InteropServices;

namespace System;

[Serializable]
[ComVisible(true)]
[__DynamicallyInvokable]
public enum StringComparison
{
	[__DynamicallyInvokable]
	CurrentCulture,
	[__DynamicallyInvokable]
	CurrentCultureIgnoreCase,
	InvariantCulture,
	InvariantCultureIgnoreCase,
	[__DynamicallyInvokable]
	Ordinal,
	[__DynamicallyInvokable]
	OrdinalIgnoreCase
}
